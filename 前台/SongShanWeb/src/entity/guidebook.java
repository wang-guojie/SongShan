package entity;

public class guidebook {
	int ticketsid;
	String ticketsname;
	float ticketsprice;
	int ticketstype;
	String remark;
	String transport;
	String imagesrc;
	ticketstype ticketstypes;
	public int getTicketsid() {
		return ticketsid;
	}
	public void setTicketsid(int ticketsid) {
		this.ticketsid = ticketsid;
	}
	public String getTicketsname() {
		return ticketsname;
	}
	public void setTicketsname(String ticketsname) {
		this.ticketsname = ticketsname;
	}
	public float getTicketsprice() {
		return ticketsprice;
	}
	public void setTicketsprice(float ticketsprice) {
		this.ticketsprice = ticketsprice;
	}
	public int getTicketstype() {
		return ticketstype;
	}
	public void setTicketstype(int ticketstype) {
		this.ticketstype = ticketstype;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getTransport() {
		return transport;
	}
	public void setTransport(String transport) {
		this.transport = transport;
	}
	public String getImagesrc() {
		return imagesrc;
	}
	public void setImagesrc(String imagesrc) {
		this.imagesrc = imagesrc;
	}
	public ticketstype getTicketstypes() {
		return ticketstypes;
	}
	public void setTicketstypes(ticketstype ticketstypes) {
		this.ticketstypes = ticketstypes;
	}
}
